char --almacenar tipo de datos de ancho fijo.

varchar --alamacena tipo de datos alfanumericos de ancho variable.

text --alamacena tipos de datos de texto.

nchar --almacenar tipos de datos de ancho (Espacios que sobran, seran eliminados).

nvarchar --almacena tipo de datos alfanumericos de ancho variable (Espacios que sobran, seran eliminados).

bit --almacena valores de 1 y 0 (Es como de tipo boolean, se podra utilizar de esa forma)

int --almacena valores entre -2,147,483,648 y 2,147,483,647.

bigint --almacena valores entre -9,223,372,036,854,775,808 and 9,223,372,036,854,775,807.

decimal --almacena valores entre -10^38 + 1 to -10^38 - 1. (unicamente es para valores decimales).

numeric --almacena valores entre -10^38 + 1 to -10^38 - 1. (nos permite aumentar de valores enteros a decimales).

money --almacena valores entre -9,223,372,036,854,775,808 and 9,223,372,036,854,775,807 (solo valor cuyo proposito sea financiero)

float --alamacena valores entre -1.79E + 308 to 1.79E + 308.


